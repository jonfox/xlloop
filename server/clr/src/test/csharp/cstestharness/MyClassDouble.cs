namespace cstestharness {
    class MyClassDouble {
        public double Sum(double d1, double d2) {
            return d1 + d2;
        }

        public double Sum(double d1, double d2, double d3) {
            return d1 + d2 + d3;
        }

        public double Square(double d) {
            return d * d;
        }
    }
}
