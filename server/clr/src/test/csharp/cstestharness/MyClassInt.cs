namespace cstestharness {
    class MyClassInt {
        public int Sum(int i, int j) {
            return i + j;
        }

        public int Sum(int i, int j, int k) {
            return i + j + k;
        }

        public int Difference(int i, int j) {
            return i - j;
        }

        public int Product(int i, int j) {
            return i*j;
        }
    }
}
